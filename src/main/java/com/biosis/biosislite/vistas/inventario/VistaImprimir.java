    private void listar() {
//        lista = this.dmc.buscarTodos();
        lista = new ArrayList();
        lista = ObservableCollections.observableList(lista);

        MTimpresion mt = new MTimpresion(lista);
        tblImprimir.setModel(mt);

        actualizarTabla();
    }

    private void actualizarTabla() {
        tblImprimir.packAll();
    }

    private void buscar() {

        lista.clear();
        if (btnInventario.isSelected()) {
            lista.addAll(this.bic.buscarXImpresion(this.inventario));
            tblImprimir.packAll();
        } else if (btnAmbiente.isSelected()) {
            lista.addAll(this.bic.buscarXImpresion(this.inventario, this.ambiente));
            tblImprimir.packAll();
        }

    }

